            <h4>Fashion Features If You’re Still Having ‘The Ankara Is Ours’ Conversations As An African, This Is For You! Ankara Has Become The Fashion Force Holding Us Together, Appropriation Talks Aside</h4>
            <p>
                If you’ve ever worn Ankara as a Nigerian/African and you join in the conversation of appropriation whenever you see a ‘non-African’ work with or wear the same, now is the time for a bit of reflection! Ankara, dubbed globally as African wax print is one
                of the most versatile and uber loved fabrics around. The prints are produced colourfully, eye catching and even more? So versatile! They are loud but the good kind of loud – and fashionably so. They are almost always usually a beauty to
                behold. They’ve become synonymous with West Africa, these print designs come in various forms but they all retain a certain richness, vibrancy, bold motifs that make them stand out every time. The Ankara print fabric is usually presented
                in twelve (12) or half/six (6) yards for sale. They were created originally as batik knockoffs for Indonesians by the Dutch but West Africa adopted the print that’s now widely claimed as African. Ankara was first introduced to the market
                as cheap mass produced imitation of Indonesian batik materials before they became synonymous with Africa – West Africa particularly – and they became easy to claim because of the vibrant prints and largely because African fashion has always
                been Avant Garde so they fit right in. Ankara print materials are made through an Indonesian wax-resist dyeing process called Batik which entails a technique to make the fabric resist the dye from getting through to the entire fabric,
                thereby creating a pattern (the ultimate print that is so desired). 
            </p>
        </section>
    </section>
        <p>
            Because of its easy cotton construction, it’s versatile, comfortable and easy to work with – making it one that can be crafted in the most unconventional styles. Known
            by different names including Kitenge in Kenya, ‘Dutch wax’ in Ghana, ‘Kanga prints’ in Tanzania and other East African countries as Ikat, Batik, mud cloth and so on, the print has arguably become a ‘global phenomenon’! And has it gotten
            out of the world recognition from foremost designers? But of course! Ankara‘s versatility, richness in colour, durability, uniqueness and timeless appeal makes it one ‘trend’ that will transcend centuries and continue catching on globally.
            Ankara has been exported all around the world, Beyonce is easily one of those who have been pictured in wax prints, making her first solid debut in Reuben Reuel’s Demestiks brand years ago. Solange also loves her some prints. Top designers
            also take inspiration for their new collections from the beautiful print. Stella Jean‘s designs are largely influenced by it, top designer Duro Olowu also uses the print in his works, Ghanaian designer Christie Brown and back in Nigeria
            the likes of Lisa Folawiyo and more have made African print a huge part of their designs. Stella McCartney faced a backlash a while back when she crafted her SS18 collection with five solid print designs inspired by this gorgeous print.
            The Dior Cruise 2020 collection is all shades of wax print, the list is endless. While yes, there are instances of cultural appropriation and we scream when we see these prints and all ‘our’ elements infused into a collection trying to
            tell an African without getting the backstory or allow the stories to be told by the ‘owners’ then calling them out is justified but when inspired to create around that just like one would with a crepe de chine or a silk crepe or even
            a Mulberry silk then by all means it’s inspiration and that’s fair (and even maybe not but can we move on?) “The Ankara fabric like religion travelled from Europe into Africa (West Africa to be precise) and made it it’s home” lead fashion
            consultant at RTFCompany and experienced fashion stylist and brand consultant Rhoda Ebun says. She continues; “History says that the Europeans used the fabric to bait West Africans into slave merchandising, this we will never know for
            sure. But one thing that is a fact is that what was popularly known as ‘Dutch Wax’ or ‘Hollandis’ is known as Ankara in the Nigerian Parlance and more popularly as African prints because the colours and designs became synonymous to African
            heritage”. “We made it our own. We made it our fashion and we made it our history! I don’t know any successful Nigerian designer who hasn’t infused Ankara into their collection or designs. Like rice, it is a staple. But like the story
            of the prodigal son, Ankara might be returning to Europe and maybe it’s time for West Africans to let this fabric fly and share it’s beauty, especially as the fabric as African as we claim it to be, is not even produced here in the first
            place”. “We cried Culture appropriation when Italian fashion designer Stella Jean infused Ankara into her collections. We ranted and raved when Christian Dior infused Ankara into it’s Cruise 2020 collection. I believe what we should be
            doing is celebrating the fact that because of US, international brands want to use this fabric. They see what we do, and they want to be part of the narrative. Culture appreciation and not appropriation is what we should be saying”. “I
            have been privileged to work as brand consultant to some fashion brands and one of these brands TISKIES whose aesthetic is predominantly Ankara produced her fabrics in Nigeria because she wanted to promote the manufacturing industry in
            Nigeria.
            If we love this fabric so much, I think we should start producing high quality fabrics we can export; and then we can now call it our own.” Rhoda Ebun, The lead fashion consultant at RTFCompany shares. A cursory look at style influencer & Creative Director
            of Style Connaisseur Angel Obasi’s Instagram page (@styleconnaisseur) will show you an endless list of stunning styles you can achieve with Ankara in all its beautiful glory and remember I mentioned super versatile? You’d see all these come
            to play. Her love for the print started out with a funny story; “I started wearing prints because I never liked wearing them. It might sound funny but it is true. You see I believe strongly as a person who needs to make an impact in the fashion
            industry, you must be multi dimensional in your taste and your style.” I wanted to push myself to be better with my style choices and fashion statement” she shares. This can easily be the case for the ‘non-African designer’ who decides to
            explore this piece for his or her creation – as long as correct references are made. But then who cares if they are just inspired? Which creative has never been? Especially when there’s so much more to explore. “Since I officially started
            my journey with Ankara, it is safe to say the fabric itself is one of the best things to happen to the fashion industry. A great fabric can make or mar the style. More often than not, when you have been the simplest of styles made with great
            Ankara print/fabric, the uniqueness cannot be missed” Obasi continues, even more reason a talented designer would also want to ‘play’ with this fabric?. Obasi who takes photos in loads of Ankara pieces shares why she seemingly loves the fabric;
            “I have lots of clients with great taste. They come up with the designs, I am their canvas/mannequin”. Obasi says Ankara “has managed to become a fashion statement both for Africans and non Africans and that in itself says everything about
            the African print”. So can we let go of this “we own our things” narrative already? Ankara has no doubt found a home in Africa but for it being claimed strictly as an African invention is laughable! And because we trust our creativity, let’s
            move on to even more discoveries…? Yep, it’s about time we woke up and let the Ankara fly. Olamide Olarewaju is a fashion editor & brand strategist.
        </p>
